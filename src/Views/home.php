<h1>
    A Sample template
</h1>

<p>
    You don't have to use native templates. You can use twig or latte or plates or whatever you want.
</p>

<p>
    Just create a render class that implements `TemplateRendererInterface`.
</p>

<p>
    If you are using Dependency Injector, replace the `DummyTemplateRenderer` with Your Own Renderer in
    `Dependencies.yaml`.
</p>

